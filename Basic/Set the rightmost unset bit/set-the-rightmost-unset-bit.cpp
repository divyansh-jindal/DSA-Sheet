class Solution
{
public:
    int setBit(int N)
    {
        // Write Your Code here
        int c=1;
        int mask=1;
        while(N&(1<<(c-1))){
            mask=1<<c;
            c++;
        }
        return N|mask;
    }
};

//{ Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int N;
        cin >> N;
        Solution ob;
        int ans = ob.setBit(N);
        cout << ans << endl;
    }
    return 0;
}

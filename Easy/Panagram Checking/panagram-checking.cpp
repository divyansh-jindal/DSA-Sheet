class Solution
{
  public:
    //Function to check if a string is Pangram or not.
    bool checkPangram (string s) {
        // your code here
        int mask = 0;
    for (char c : s) {
        if (c >= 'A' && c <= 'Z') mask |= (1 << (c-'A'));
        else if (c >= 'a' && c <= 'z') mask |= (1 << (c-'a'));
    }
    return mask == (1 << 26) - 1;
    }

};

